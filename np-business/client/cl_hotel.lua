end)

function ensurePermission(business)
  local hasPermission = HasPermission(business, "craft_access")
  if not hasPermission then
    TriggerEvent("DoLongHudText", "You do not have permission for this.", 2)
    return false
  end
  return true
end

function getHotelRoomsListOptions(biz)
  local rooms = getHotelRooms(biz)
  local options = {}
  for k, v in ipairs(rooms) do
    options[#options + 1] = { id = v.id, name = v.name }
  end
  return options
end

function getHotelRooms(biz)
  local rooms = {};
  for k, v in ipairs(spawnLocations) do
    if v.biz == biz then
      rooms[#rooms + 1] = v
    end
  end
  return rooms
end

function getHotelRoomSpawn(roomId)
  for k, v in pairs(spawnLocations) do
    if v.id == roomId then
      return v
    end
  end
end

function getRoomsForCid(cid)
  local roomIds = RPC.execute("np-business:hotel:rpc:getRooms", cid)
  local rooms = {};
  for k, v in pairs(roomIds) do
    rooms[#rooms + 1] = getHotelRoomSpawn(v)
  end
  return rooms
end

-- List tenants

AddEventHandler("np-business:hotel:listTenantsPrompt", function(args)
  local biz = args.biz
  if not ensurePermission(biz) then return end
  exports["np-ui"]:openApplication("textbox", {
    callbackUrl = "np-business:hotel:ui:listTenants",
    key = "np-business:hotel:ui:listTenants",
    items = {
      {
        _type = "select",
        options = getHotelRoomsListOptions(biz),
        icon = "user-edit",
        label = "Room",
        name = "room",
      }
    },
    show = true,
  })
end)

RegisterUICallback("np-business:hotel:ui:listTenants", function(data, cb)
  cb({ data = {}, meta = { ok = true, message = "" } })
  exports["np-ui"]:closeApplication("textbox")
  local tenants = RPC.execute("np-business:hotel:rpc:getTenants", data.values.room)
  if #tenants == 0 then
    TriggerEvent("chatMessage", "Tenants", { 30, 144, 255 }, "This room has no tenants", "feed", false, { i18n = { "This room has no tenants" }})
  else
    TriggerEvent("chatMessage", "Tenants", { 30, 144, 255 }, table.concat(tenants, ", "))
  end
end)


